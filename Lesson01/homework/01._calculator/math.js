class Math {
  constructor(a, b) {
    this.a = a;
    this.b = b;
  }

  sumOfTwoNumbers(a, b) {
    const sum = a + b;
    return sum;
  }

  subtraction(a, b) {
    const sum = a - b;
    return sum;
  }

  multiply(a, b) {
    const sum = a * b;
    return sum;
  }

  division(a, b) {
    const sum = a / b;
    return sum;
  }
}

export default Math;
